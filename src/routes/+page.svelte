<article> 
	Climate change, a pressing global issue, is primarily driven by the increase in greenhouse gas emissions that envelop our planet. These gases, including carbon dioxide and methane, act like a blanket, trapping the sun's heat within the Earth's atmosphere, a phenomenon known as the greenhouse effect. This process results in global warming, a significant rise in the Earth's average temperature, which in turn instigates drastic changes in our climate. These changes include more frequent and severe weather events, such as hurricanes and droughts, melting polar ice caps, and rising sea levels. Alarmingly, scientific data indicates that our world is currently experiencing a rate of warming that is unprecedented in recorded history. This rapid warming underscores the urgency of addressing climate change and reducing greenhouse gas emissions to protect our planet for future generations.

</article>

	<details style="padding-top: 20px;">
		<summary role="button">Why is climate change bad?</summary>
		<ul>
			<li>Rising global temperatures: The Earth's average temperature is increasing, leading to hotter weather and heatwaves.</li>
			<li>Melting ice caps and glaciers: Warmer temperatures are causing the polar ice caps and glaciers to melt at an accelerated rate.</li>
			<li>Rising sea levels: The melting ice contributes to rising sea levels, which can lead to coastal flooding and the loss of land.</li>
			<li>More frequent and severe weather events: Climate change can lead to more intense storms, hurricanes, and extreme rainfall, as well as more severe droughts and heatwaves.</li>
			<li>Changes in precipitation patterns: Some areas may experience more rainfall while others may become drier, affecting agriculture and water availability.</li>
			<li>Shifts in wildlife populations and habitats: Changes in temperature and precipitation can disrupt ecosystems and lead to shifts in wildlife populations and habitats. Some species may become extinct if they cannot adapt quickly enough.</li>
			<li>Ocean acidification: Increased carbon dioxide in the atmosphere is absorbed by the oceans, leading to acidification which harms marine life, particularly shellfish and coral reefs.</li>
			<li>Impact on human health: Climate change can lead to increased heat-related illnesses and deaths, worsened air quality, and the spread of disease-carrying insects to new areas.</li>
			<li>Economic impacts: Damage from extreme weather events, changes in agricultural productivity, and health impacts can have significant economic costs.</li>
		</ul>
